#include "useragentprovider.h"
#include <QDebug>

UserAgentProvider::UserAgentProvider(QWidget *parent) :
    QWebPage(parent)
{
}

QString UserAgentProvider::userAgentForUrl ( const QUrl & url ) const
{
    qDebug() << url;
    return "Mozilla/5.0 (Windows NT 6.1; WOW64) AppleWebKit/535.2 (KHTML, like Gecko) Chrome/15.0.874.121 Safari/535.2";
}

QString UserAgentProvider::getUserAgent()
{
    return userAgentForUrl(QUrl(""));
}
